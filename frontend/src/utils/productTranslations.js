/**
 * Get translated product name
 * @param {string} productName - Original product name (usually in English)
 * @param {string} locale - Target locale (en, am, or)
 * @returns {string} - Translated product name or original if no translation found
 */
export function translateProductName(productName, locale = 'en') {
  if (!productName) return ''
  
  // Check for exact match
  if (productNameTranslations[productName]) {
    return productNameTranslations[productName][locale] || productName
  }
  
  // Check for partial match (case-insensitive)
  const lowerName = productName.toLowerCase()
  for (const [key, translations] of Object.entries(productNameTranslations)) {
    if (lowerName.includes(key.toLowerCase())) {
      // If it's a partial match, try to preserve the format
      const translation = translations[locale]
      if (translation && key !== productName) {
        // Replace the matched part with translation
        return productName.replace(new RegExp(key, 'gi'), translation)
      }
      return translation || productName
    }
  }
  
  // Return original if no translation found
  return productName
}

